package com.example.mykloud.models;

public class ListModel {
    private Long createdAt;
    private String name;
    private String priority;
    private String id;


    public Long getCreatedAt() {
        return createdAt;
    }

    public String getName() {
        return name;
    }

    public String getPriority() {
        return priority;
    }

    public String getId() {
        return id;
    }
}
